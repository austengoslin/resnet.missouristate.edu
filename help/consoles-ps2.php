            <h2>Sony PS2 Registration and Configuration</h2>
            <p class="answer">
              <em>There are two versions of the PlayStation 2: a slimline version manufactured after September 2004, and the original PS2.</em>
            </p>
           
            
            
            <h3>Slimline PS2</h3>
            <ol class="indent"><li>
                    Turn on your PlayStation 2 console with no game disc in the disc tray.
                </li>
                <li>
                    From the main screen, press the <strong>triangle</strong> button.
                </li>
                <li>
                    This will bring up the <strong>System Information</strong> screen where the 12-character MAC address will be listed.  
                    <br />
                    (<em>Double check the MAC address. 
                    On a TV screen it is easy to confuse the letter D with the number 0 and the letter B with the number 8.</em>)
                </li>
            </ol>
            <h3>Original PS2</h3>
            <ol class="indent">
            	<li>
                    Power up the PlayStation 2 and load the Network Adapter Startup Disc.
                </li>
                <li>Once is has loaded, select <strong>ISP Setup</strong> and press X.</li>
                <img class="imgBorder" src="../images/consoles/consoles-ps2-1.jpg" alt="PS2 Settings 1" width="500" height="375">
                <li>The ISP Setup will begin to load, and you will be presented with a license agreement.  Select <strong>Agree</strong> and press X to continue.</li>
                <img class="imgBorder" src="../images/consoles/consoles-ps2-2.jpg" alt="PS2 Settings 2" width="500" height="375">
                <li>For connection type, choose <strong>Local Area Network (LAN)</strong> and press X to continue.</li>
                <img class="imgBorder" src="../images/consoles/consoles-ps2-3.jpg" alt="PS2 Settings 3" width="500" height="375">
                <li>Select <strong>Automatic Configuration</strong> and press X to continue.</li>
                    <img class="imgBorder" src="../images/consoles/consoles-ps2-4.jpg" alt="PS2 Settings 4" width="500" height="375">
                <li> At the connection test screen, select <strong>Advanced</strong> and press X to continue.</li>
                    <img class="imgBorder" src="../images/consoles/consoles-ps2-5.jpg" alt="PS2 Settings 5" width="500" height="375">
                <li>
                    At the Advanced Broadband Settings screen, make sure <strong>Automatic</strong> is selected.  
                    While you are at this screen, make note of the <strong>MAC address</strong> at the bottom.  
                    You will need this number to register your game console.
                    <br />
                    (<em>Double check the MAC address. 
                    On a TV screen it is easy to confuse the letter D with the number 0 and the letter B with the number 8.</em>)
                   </li>
                    <img class="imgBorder" src="../images/consoles/consoles-ps2-6.jpg" alt="PS2 Settings 6" width="500" height="374">                
            </ol>
