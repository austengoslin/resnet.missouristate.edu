            <h3>Print Quota</h3>
            <p>
                In an effort to enhance your ability to print to the front desk printers and to help conserve precious resources, 
                an automated laser printing quota system was instituted beginning the 2006 Spring semester.  
                Below are a few important highlights:
            </p>
            <ul>
				<li>
                    Each residence hall student is able to print up to 350 pages per semester. 
                    This amount will likely meet all of your printing needs and will hopefully encourage conservation.
                    There is no monetary value associated with these 350 pages. 
                </li>
                <li>
                    Individual print requests are limited to 25 pages and 50mb. 
                    This is intended to keep the printers moving quickly; jobs that require more than 25 pages, 
                    or that are larger than 50mb will need to be broken into parts and printed separately.
                    There is no limit on the number of seperate requests for each print job, however, they must be at least 10 seconds apart.
 				 </li>
                <li>
                    If you run out of your allotted pages per semester, you may purchase additional pages (in quantities of 100) 
                    for $5.00 from the Department of Residence Life, Housing and Dining Services, located in Hammons House #104.  
                    This office is open during normal business hours.
 				 </li>
                <li>
                    To view information on your print quota, please visit 
                    <a href="http://resprint.missouristate.edu" title="Resprint">http://resprint.missouristate.edu</a> and log in 
                    using your BearPass login and password.
 				 </li>
                <li>
                    On all residence hall lab computers there is an icon near the system tray that allows quick 
                    checking of your balance of pages, as well as provide you with a link to a more detailed summary of your account.  
                    In addition, messages regarding approved or denied print jobs will pop up from this icon.
 				 </li>
                <li>
                    All print jobs submitted to the front desk laser printer, regardless of location, are counted against your quota.  
                    The cover page does not count toward your quota.
                </li>
                <li>
                    The print quota system sends an email notification if a print job is denied.  
                    Should you have difficulty printing, please check your account at 
                  <a href="http://resprint.missouristate.edu" title="Resprint">http://resprint.missouristate.edu</a> or your university email account for information.
                </li>
            </ul>
            <h3>Duplex (Double-Sided) Printing</h3>
            <p class="indent">
                As of October 9th, 2008, all of the Residence Hall front desk printers print on both sides of the paper.
                The University decided to make this the default setting as a means to conserve natural resources and maximize efficiency.
                In addition, it makes for better use of students' print quota, since the quota is based off of the number of pages physically printed, 
                as opposed to pages that appear in the document.
                This will effectively double the amount of printing a student can do.
                <br /><br />
                
            <strong>Note:</strong> <em>Macintosh users must manually select the "Two-Sided" option to print double-sided. See our <a href="../help/printerSetup.php" title="printer setup tutorial"> printer setup tutorial</a> for more information.</em>
            </p>

