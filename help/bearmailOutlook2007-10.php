            <h3>Outlook 2007 and 2010 Setup for Live@edu</h3>
			<p>This guide works if you are currently living on-campus. If you are currently setting up Outlook from off-campus, please follow <a href="#OffCampus">this guide</a>.
			</p>
            <ol class="indent">
				<li>Before you begin setting up Outlook, you should ensure you are using the correct password. Open up an Internet browser and go to <a href="http://outlook.office365.com" target="_blank">Outlook.com</a>
					<ul class="normal">
						<li>For your Windows Live ID, enter your <strong>BearPass login</strong> followed by <strong>@live.missouristate.edu</strong></li>
                        <li>For your Password, enter your Live@EDU E-mail password. If it does not work, you may need to change your password at <a href="https://cams.missouristate.edu/selfservice/changepassword.aspx">cams.missouristate.edu</a></li>                        						
					</ul><br />
					  <img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2010-LiveEdu-06a.PNG" alt="Web Interface Address" width="323" height="350">                						  
				</li>				
				<li>Make sure Outlook is closed, then click on "Start" &raquo; "Control Panel"</li>
				<li>Open up Mail (it will be listed as mail 32bit on 64bit systems).<br /><img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2007-LiveEdu-01.PNG" alt="Control Panel with Mail 32"></li>                
				<li>Select Email Accounts.<br /><img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2007-LiveEdu-02.PNG" alt="Main 'mail' box"></li>		      
				<li>Select New<br /><img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2007-LiveEdu-03.PNG" alt="Account settings" width="598" height="494"></li>					                    </li>                    
				<li>Enter the following Microsoft Exchange Settings:
                    	<ul class="normal">
							<li>Name: <strong>Your Name</strong></li>
                        	<li>E-mail Address: <strong>BearPass Login@live.missouristate.edu</strong></li>
                        	<li>And enter the password you've created twice</li>
                   	  </ul><br /><img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2007-LiveEdu-04.PNG" alt="Main mail configuration screen" width="595" height="453">					
				</li>					
                    <li>It will search for the settings. <br /><img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2007-LiveEdu-05.PNG" alt="Connecting to Server" width="588" height="456"></li>					
                   	<li>You may get a dialog box asking if you would like to allow live.missouristate.edu to make changes. <strong>Click allow.</strong></li>
                    <li>A login box will prompt you for creditials. Make sure it has your <strong>BearPass Login</strong> in the username field. Then retype your password.<br /><img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2007-LiveEdu-06.PNG" alt="Username and Password Required"></li>
                    <li>Congratulations you have completed the setup! Load Outlook to verify it worked.  <br /><img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2007-LiveEdu-07.png" alt="You're finished!" width="596" height="448"></li>					
            </ol>
			<a name="OffCampus"><h3>Outlook 2007 and 2010 Setup for Live@edu from Off-campus</h3></a>
			<ol class="indent">
            	<li>Make sure Outlook is closed, then click on "Start" &raquo; "Control Panel"</li>                
                <li>Open up Mail (it will be listed as mail 32bit on 64bit systems).<br /><img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2007-LiveEdu-01.PNG" alt="Control Panel with Mail 32"></li>					
                <li>Select Email Accounts.<br /><img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2007-LiveEdu-02.PNG" alt="Main 'mail' box"></li>					
                <li>Select New<br /><img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2007-LiveEdu-03.PNG" alt="Account settings" width="598" height="494"></li>					                    
                <li>Select Manually configure server settings or additional server types<br /><img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2010-LiveEdu-04.PNG" alt="Main mail configuration screen" width="687" height="478"></li>										
                <li>Select Microsoft Exchange or compatible service<br /><img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2010-LiveEdu-05.PNG" alt="Select Microsoft Exchange" width="687" height="478"></li>										
                <li>Open up an Internet Browser, go to <a href="http://outlook.com">Outlook.com</a>
					<ul class="normal">
						<li>For your Windows Live ID, enter your <strong>BearPass login</strong> followed by <strong>@live.missouristate.edu</strong></li>
                        <li>For your Password, enter your Live@EDU E-mail password. If it does not work, you may need to change your password at <a href="https://cams.missouristate.edu/selfservice/changepassword.aspx">cams.missouristate.edu</a></li>                        						
					</ul><br />
					<img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2010-LiveEdu-06a.PNG" alt="Web Interface Address" width="323" height="350">					  
				</li>
                <li>In the URL, highlight and copy the name of the server, as shown below.  It will likely not be the same as the one shown here.<br /><img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2010-LiveEdu-06.PNG" alt="Outlook URL" width="530" height="107"></li>					
                <li>Return to the Outlook configuration screen. 
					<ul class="normal">
						<li>Paste the URL you copied previously and add <strong>.mailbox.outlook.com</strong> to the end of it</li>
						<li>Enter your <strong>BearPass ID</strong> into the User Name field</li>
						<li>Then, click on More Settings...</li>
					</ul>
					<img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2010-LiveEdu-07.PNG" alt="Server Settings" width="687" height="478">					
                </li>
				<li>Check the box to Connect to Microsoft Exchange using HTTP then click on the Exchange Proxy Settings...<br /><img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2010-LiveEdu-08.PNG" alt="Connection Tab" width="354" height="439"></li>
                <li>In the Window for Microsoft Exchange Proxy Settings, configure the following:
					<ul class="normal">
						<li>Next to https:// type: <strong>pod51000.outlook.com</strong></li>
						<li>Check the box Only connect to proxy servers that have the principal name in their certificate:</li>
						<li>Type <strong>msstd:outlook.com</strong> in the box below Only Connect...</li>
						<li>Check the box beside On fast networks, connect using HTTP first, then connect using TCP/IP</li>
						<li>Change Proxy authentication settings to <strong>Basic Authentication</strong></li>
						<li>Click OK</li>
					</ul>
					<img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2010-LiveEdu-09.PNG" alt="Select Microsoft Exchange" width="462" height="411">	
				</li>										
                <li>A popup should appear asking for your credentials. Enter your BearPass login, followed by <strong>@live.missouristate.edu</strong>, and your password<br /><img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2010-LiveEdu-10.PNG" alt="Login Window" width="439" height="261"></li>											
                <li>You should be returned to the previous screen, except now your BearPass E-mail will be in the User Name box, and everything will be underlined. Click Next<br /><img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2010-LiveEdu-11.PNG" alt="Information Underlined" width="687" height="478"></li>
                <li>A warning message may appear if this is not your first Outlook account.  Just click OK<br /><img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2010-LiveEdu-12.PNG" alt="Possible Warning Message" width="751" height="175"></li>										
                <li>A window should appear letting you know that everything went through successfully.  Click Finish</br> <img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2010-LiveEdu-13.PNG" alt="Congratulations Screen" width="687" height="478"></li>										
                <li>When opening Outlook, you'll be asked to enter your credentials, then it should open.  Congratulations, you have now gotten Outlook successfully set up.<br /><img class="imgBorder" src="../images/bearmail/outlook07/bearmail-outlook2010-LiveEdu-14.PNG" alt="Login Window on Outlook Startup" width="477" height="282"></li>                					
            </ol>                   
