            <h2>Choosing the Right Network Cable</h2>
            <p>When connecting your computer to the network, you must use a network cable (also known as an Ethernet or CAT-5 cable). 
                A regular phone cable will not work, even though it looks similar to a network cable.
               <br /><br />
               Network cables can be purchased at the <a href="http://bookstore.missouristate.edu">Missouri State Bookstore</a>.
            </p>
            <h3>Some Key Phone and Network Cable Differences</h3>
            <ul><li>Network cables typically have a rounder shape and are thicker than phone cables.</li>
                <li>Network cables have larger connectors than phone cables.</li>
                <li>Network cables have more wires and metal pins than phone cables.  
                    <br /><em>(Network cables always have 8 wires, while phone cables have either have 2 or 4.)</em></li>
            </ul>
            <p>
                After you have verified that you have a network cable, connect one end into the Ethernet card located in your computer and the other into the network jack located in your room.  
                Typically, this jack will be orange in color and will be labeled with a number, such as -2 or -4.  
                Once connected, make sure to register your device at <a href="http://resreg.missouristate.edu" title="ResReg">http://resreg.missouristate.edu</a>.
            </p><br />
