                <h2>Copyright and File-Sharing Information</h2>

                <h3>What is File-Sharing and Why is it Bad for the Network?</h3>
                <p class="indent">
            File-sharing is the transfer of files over a computer network, usually via a peer-to-peer (P2P) program where files are stored on individuals' computers rather than a central server location.
            Common P2P programs include Limewire, BitTorrent, uTorrent, eMule, Bearshare, and others.
            What this means is that people with the above listed programs can share files from their computers and download files on other users' computers.
                    <br /><br />
            Problems arise because as long as the program is running, a large number of users can be connected to that computer at one time, all downloading.
            So even though a single file might be small and of little consequence, the number of people connecting to get that one particular file can be a big detriment on the network and use up a large amount of bandwidth.
            In addition to that, some programs may route local traffic through a user's computer, even when no files are shared from that computer, compounding the problems listed above.
                </p>

                <h3>What Legal Concerns Accompany File-Sharing?</h3>
                <p class="indent">
            While the sharing of files from one computer to another is perfectly legal, the sharing of copyrighted material without the consent of the copyright holder is illegal.
            This includes most music, movies, and television shows that are played on the radio and television.
            If the copyright owner or an entity representing them discovers that someone is sharing their material via a P2P program, they are within their rights to pursue legal action against that person, which may include suing the individual for monetary damages.
            If you are unsure of whether or not you have permission to share a file, we recommend that you err on the side of caution and avoid doing anything potentially illegal.
                </p>

                <h3>What Does this Mean for Residence Hall Students?</h3>
                <p class="indent">
            Due to the potential problems that accompany using file-sharing programs, we recommend that all residence hall students uninstall them.
            Since many of them start automatically with the computer and will share files without the user's knowledge, refraining from use of the program may be insufficient.
                </p>

                <h3>Are there Legal Ways to Discover New Music?</h3>
